def sum_list(items):
    total = 0  # Initialize the total sum to 0
    for item in items:  # Iterate over each item in the list
        total += item  # Add the current item to the total
    return total  # Return the tota
my_list = [10, 20, 30, 40]
result = sum_list(my_list)
print(f"The sum of all items in the list is: {result}")



